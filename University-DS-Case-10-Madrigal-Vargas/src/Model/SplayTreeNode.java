package Model;

public class SplayTreeNode {
	private SplayTreeNode rightChild;
	private SplayTreeNode leftChild;
	
	public SplayTreeNode() {}

	public SplayTreeNode getRightChild() {
		return rightChild;
	}

	public void setRightChild(SplayTreeNode rightChild) {
		this.rightChild = rightChild;
	}

	public SplayTreeNode getLeftChild() {
		return leftChild;
	}

	public void setLeftChild(SplayTreeNode leftChild) {
		this.leftChild = leftChild;
	}
	
	
}
